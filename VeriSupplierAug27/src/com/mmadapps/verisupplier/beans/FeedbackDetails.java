package com.mmadapps.verisupplier.beans;

public class FeedbackDetails {
	
	private String mCreatedDate;
	private String mFeedBackText;
	private String mTotalFeedBack;
	private String mFeedbackRating;
	private String mFeedbackUserName;
	private String mFeedbackLocation;
	
	public String getmFeedbackLocation() {
		return mFeedbackLocation;
	}
	public void setmFeedbackLocation(String mFeedbackLocation) {
		this.mFeedbackLocation = mFeedbackLocation;
	}
	public String getmFeedbackRating() {
		return mFeedbackRating;
	}
	public void setmFeedbackRating(String mFeedbackRating) {
		this.mFeedbackRating = mFeedbackRating;
	}
	public String getmFeedbackUserName() {
		return mFeedbackUserName;
	}
	public void setmFeedbackUserName(String mFeedbackUserName) {
		this.mFeedbackUserName = mFeedbackUserName;
	}
	public String getmCreatedDate() {
		return mCreatedDate;
	}
	public void setmCreatedDate(String mCreatedDate) {
		this.mCreatedDate = mCreatedDate;
	}
	public String getmFeedBackText() {
		return mFeedBackText;
	}
	public void setmFeedBackText(String mFeedBackText) {
		this.mFeedBackText = mFeedBackText;
	}
	public String getmTotalFeedBack() {
		return mTotalFeedBack;
	}
	public void setmTotalFeedBack(String mTotalFeedBack) {
		this.mTotalFeedBack = mTotalFeedBack;
	}

}
