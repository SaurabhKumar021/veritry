package com.mmadapps.verisupplier.beans;

public class InventoryWarehouseList {
	
	private String warehousename;
	private String availablequantity;
	private String availableproductunit;
	private String warehouseid;
	
	public String getWarehouseid() {
		return warehouseid;
	}
	public void setWarehouseid(String warehouseid) {
		this.warehouseid = warehouseid;
	}
	public String getWarehousename() {
		return warehousename;
	}
	public void setWarehousename(String warehousename) {
		this.warehousename = warehousename;
	}
	public String getAvailablequantity() {
		return availablequantity;
	}
	public void setAvailablequantity(String availablequantity) {
		this.availablequantity = availablequantity;
	}
	public String getAvailableproductunit() {
		return availableproductunit;
	}
	public void setAvailableproductunit(String availableproductunit) {
		this.availableproductunit = availableproductunit;
	}
	
	
	

}
