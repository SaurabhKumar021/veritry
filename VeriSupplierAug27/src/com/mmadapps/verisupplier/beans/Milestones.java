package com.mmadapps.verisupplier.beans;

public class Milestones {

	private String mQuotationHistoryId;
	private String mName;
	private String mBriefDescription;
	private String mPercentBreakup;

	public String getmQuotationHistoryId() {
		return mQuotationHistoryId;
	}

	public void setmQuotationHistoryId(String mQuotationHistoryId) {
		this.mQuotationHistoryId = mQuotationHistoryId;
	}

	public String getmName() {
		return mName;
	}

	public void setmName(String mName) {
		this.mName = mName;
	}

	public String getmBriefDescription() {
		return mBriefDescription;
	}

	public void setmBriefDescription(String mBriefDescription) {
		this.mBriefDescription = mBriefDescription;
	}

	public String getmPercentBreakup() {
		return mPercentBreakup;
	}

	public void setmPercentBreakup(String mPercentBreakup) {
		this.mPercentBreakup = mPercentBreakup;
	}

}
