package com.mmadapps.verisupplier.beans;

public class ProductVariantOptions {

	private String mCheckedState;
	private String mCount;
	private String mName;

	public String getmCheckedState() {
		return mCheckedState;
	}

	public void setmCheckedState(String mCheckedState) {
		this.mCheckedState = mCheckedState;
	}

	public String getmCount() {
		return mCount;
	}

	public void setmCount(String mCount) {
		this.mCount = mCount;
	}

	public String getmName() {
		return mName;
	}

	public void setmName(String mName) {
		this.mName = mName;
	}

}
